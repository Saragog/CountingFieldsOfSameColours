
public class Field {

	public int xPosition;
	public int yPosition;
	public int colour;
	public boolean wasUsed;
	
	public Field(int xPosition, int yPosition, int colour, boolean wasUsed)
	{
		this.xPosition = xPosition;
		this.yPosition = yPosition;
		this.colour = colour;
		this.wasUsed = wasUsed;
	}
}
